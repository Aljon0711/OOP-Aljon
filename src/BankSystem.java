    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        String name = JOptionPane.showInputDialog(this, "Enter your name:");
    if (name == null || name.trim().isEmpty()) {
        JOptionPane.showMessageDialog(this, "Name cannot be empty. Deposit canceled.");
        return;
    }
    
    String amountStr = JOptionPane.showInputDialog(this, "Enter amount to deposit:");
    if (amountStr == null || amountStr.trim().isEmpty()) {
        JOptionPane.showMessageDialog(this, "Amount cannot be empty. Deposit canceled.");
        return;
    }
    
    double amount;
    try {
        amount = Double.parseDouble(amountStr);
    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(this, "Invalid amount format. Deposit canceled.");
        return;
    }
    
    int confirm = JOptionPane.showConfirmDialog(this, "Are you sure you want to deposit P " + amount + "?");
    if (confirm != JOptionPane.YES_OPTION) {
        JOptionPane.showMessageDialog(this, "Deposit canceled.");
        return;
    }
    
    // Add the deposit amount to the balance
    balance += amount;
    
    JOptionPane.showMessageDialog(this, "You have deposited P " + amount + ". Deposit successful.\nYour new balance is: P " + balance);
    }//GEN-LAST:event_jButton2ActionPerformed

