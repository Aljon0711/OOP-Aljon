    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
         String name = JOptionPane.showInputDialog(this, "Enter receiver's name:");
    if (name == null || name.trim().isEmpty()) {
        JOptionPane.showMessageDialog(this, "Name cannot be empty. Transfer canceled.");
        return;
    }
    
    String idNumberStr = JOptionPane.showInputDialog(this, "Enter receiver's ID number (format: xxx-xxx):");
    if (idNumberStr == null || idNumberStr.trim().isEmpty()) {
        JOptionPane.showMessageDialog(this, "ID number cannot be empty. Transfer canceled.");
        return;
    }
    
    // Validate ID number format
    if (!idNumberStr.matches("\\d{3}-\\d{3}")) {
        JOptionPane.showMessageDialog(this, "Invalid ID number format. Format should be xxx-xxx. Transfer canceled.");
        return;
    }
    
    String amountStr = JOptionPane.showInputDialog(this, "Enter amount to transfer:");
    if (amountStr == null || amountStr.trim().isEmpty()) {
        JOptionPane.showMessageDialog(this, "Amount cannot be empty. Transfer canceled.");
        return;
    }
    
    double amount;
    try {
        amount = Double.parseDouble(amountStr);
    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(this, "Invalid amount format. Transfer canceled.");
        return;
    }
    
    if (amount > balance) {
        JOptionPane.showMessageDialog(this, "Not enough balance to transfer. Transfer canceled.");
        return;
    }
    
    int confirm = JOptionPane.showConfirmDialog(this, "Are you sure you want to transfer P " + amount + " to " + name + " (ID: " + idNumberStr + ")?");
    if (confirm != JOptionPane.YES_OPTION) {
        JOptionPane.showMessageDialog(this, "Transfer canceled.");
        return;
    }
    
    // Subtract the transfer amount from the balance
    balance -= amount;
    
    JOptionPane.showMessageDialog(this, "Transfer successful. P " + amount + " transferred to " + name + " (ID: " + idNumberStr + ").\nYour remaining balance is: P " + balance);
    }//GEN-LAST:event_jButton4ActionPerformed

