    private void loadBalance() {
        try (BufferedReader br = new BufferedReader(new FileReader("balance.txt"))) {
            String balanceStr = br.readLine();
            if (balanceStr != null) {
                balance = Double.parseDouble(balanceStr);
            }
        } catch (IOException | NumberFormatException e) {
            e.printStackTrace();
        }
    }

    // Method to save balance to file
    private void saveBalance() {
        try (BufferedWriter bw = new BufferedWriter(new FileWriter("balance.txt"))) {
            bw.write(Double.toString(balance));
        } catch (IOException e) {
            e.printStackTrace();
        }
    }
